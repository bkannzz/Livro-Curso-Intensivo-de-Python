cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort() # ordena sua lista na ordem alfabética permanentemente
print(cars)

cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort(reverse=True) # ordena sua lista na ordem alfabética reversa
print(cars)

cars = ['bmw', 'audi', 'toyota', 'subaru']

print('Here is the original list: ')
print(cars)

print('Here is the sorted list: ')
print(sorted(cars)) # ordena a lista alfabética mas não altera a ordem original

print('Here is the original list again: ')
print(cars)

cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.reverse() # reverte a lista permanentemente
print(cars)

cars = ['bmw', 'audi', 'toyota', 'subaru']
print(len(cars)) # identifica quantos elementos tem na lista